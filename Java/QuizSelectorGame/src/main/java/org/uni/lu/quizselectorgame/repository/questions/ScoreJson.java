package org.uni.lu.quizselectorgame.repository.questions;

public class ScoreJson {

    private String employeeManagement;
    private String logicalAccess;
    private String awarenessAndCompliance;
    private String informationSystem;
    private String localAreaNetwork;
    private String thirdPartyManagement;

    public String getEmployeeManagement() {
        return employeeManagement;
    }

    public void setEmployeeManagement(String employeeManagement) {
        this.employeeManagement = employeeManagement;
    }

    public String getLogicalAccess() {
        return logicalAccess;
    }

    public void setLogicalAccess(String logicalAccess) {
        this.logicalAccess = logicalAccess;
    }

    public String getAwarenessAndCompliance() {
        return awarenessAndCompliance;
    }

    public void setAwarenessAndCompliance(String awarenessAndCompliance) {
        this.awarenessAndCompliance = awarenessAndCompliance;
    }

    public String getInformationSystem() {
        return informationSystem;
    }

    public void setInformationSystem(String informationSystem) {
        this.informationSystem = informationSystem;
    }

    public String getLocalAreaNetwork() {
        return localAreaNetwork;
    }

    public void setLocalAreaNetwork(String localAreaNetwork) {
        this.localAreaNetwork = localAreaNetwork;
    }

    public String getThirdPartyManagement() {
        return thirdPartyManagement;
    }

    public void setThirdPartyManagement(String thirdPartyManagement) {
        this.thirdPartyManagement = thirdPartyManagement;
    }
}
